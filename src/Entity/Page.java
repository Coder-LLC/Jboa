package Entity;

public class Page {
    private int start=0;
    private int size=0;
    private int totalpage=0;
    
	public int getStart() {
		return start;
	}
	
	public void setStart(int start) {
		this.start = start;
	}
	
	public int getSize() {
		return size;
	}
	
	public void setSize(int size) {
		this.size = size;
	}
	
	public int getTotalpage() {
		return totalpage;
	}
	
	public void setTotalpage(int totalpage) {
		this.totalpage = totalpage;
	}
    
    
}
